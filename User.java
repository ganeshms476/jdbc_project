package jdbc_prc_userdb1;

public class User {
	private int id;
	private String username;
	private String email;
	private String password;
	private String address;
	private String fbpassword;
	private String instapassword;
	private String snappassword;
	private String whatsapppassword;
	private String twitterpassword;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getFbpassword() {
		return fbpassword;
	}

	public void setFbpassword(String fbpassword) {
		this.fbpassword = fbpassword;
	}

	public String getInstapassword() {
		return instapassword;
	}

	public void setInstapassword(String instapassword) {
		this.instapassword = instapassword;
	}

	public String getSnappassword() {
		return snappassword;
	}

	public void setSnappassword(String snappassword) {
		this.snappassword = snappassword;
	}

	public String getWhatsapppassword() {
		return whatsapppassword;
	}

	public void setWhatsapppassword(String whatsapppassword) {
		this.whatsapppassword = whatsapppassword;
	}

	public String getTwitterpassword() {
		return twitterpassword;
	}

	public void setTwitterpassword(String twitterpassword) {
		this.twitterpassword = twitterpassword;
	}
}
